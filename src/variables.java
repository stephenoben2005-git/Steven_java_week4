public class variables {
    public static void main(String[] args) {
        // Declare Variables
        int age;

        // Assignment of values
        age = 30;

        // Initialisation of Variables
        String name = "John";

        // Data type
        // Primitive data type
        // byte=> -128 to 127
        byte num1 = 124;

        // short => -32,768 to 32,767
        short num2 = -32768;

        // Int => 2,147,483,784
        int num3 = 2_134_567_899;

        // Long => 9_000_000_000_000_000_000
        long num4 = 9_123_113_456_456_110L;

        // Float => 5 to 6 dp
        float num5 = 3456.678F;

        // Double => 15dp
        double num6 = 123.456;

        //char => "A",
        char ch = 'a';

        // Boolean => true or false
        boolean isAStudent = true;


        // Non-Primitive data type
        // String
        String name1 = "Bob";

        // name, MyName, WhatIsYourName,
        //Name, MyName, WhatIsYourName,
        //name, my_name, what_is_your_name

        // casting
        //Automatic
        int a = 10;
        int b =(int) a;
        System.out.println(b);

    }
}
